void readCSV(FILE *file, struct csv_list *clist) {
    char line1[5000];
    char line2[5000];
    char line3[5000];
    char *stptr;
    int idx = 0;
    int lcount = 0;


    while (fgets(line1,sizeof line1,file) != ((void *)0)) {
 lcount = 0;


 if (line1[strlen(line1) - 1] == '\n') {
     line1[strlen(line1) - 1] = '\0';
 }

 strcpy(line2,line1);
 stptr = line2;

 add_row(clist);


 if (line1[0] == '%')
     continue;


 while (*stptr != '\0')
     { lcount++;

  if (*stptr == '"')
      {
   int flag = 0;
   idx = 0;
   while (flag == 0)
       {
    stptr++;

    while (*stptr != '"')
        { line3[idx] = *stptr;
     idx++;
     stptr++;
        }
    stptr++;
    if (*stptr != '\0' && *stptr == ',')
        {
     line3[idx] = '\0';
     add_column(clist, line3);
     flag = 1;
        }
    else if (*stptr != '\0' && *stptr == '"')
        { line3[idx] = *stptr;
     idx++;
        }
    else
        {
     line3[idx] = '\0';
     add_column(clist, line3);
     flag = 1;
        }
       }
      }
  else
      { idx = 0;
   while (*stptr != '\0' && *stptr != ',')
       { line3[idx] = *stptr;
    idx++;
    stptr++;
       }
   line3[idx] = '\0';
   add_column(clist, line3);
      }
  if (*stptr != '\0' && *stptr == ',')
      stptr++;
  memmove(line2, stptr, strlen(stptr)+1);
  stptr = line2;
     }
    }
}


