unsigned char readCSV(char *filename, struct csv_list *clist) {
    FILE *file;
    char line1[5000];
    char line2[5000];
    char line3[5000];
    char *stptr;
    int idx = 0;
    int lcount = 0;

    if (!(file = fopen_unlocked(filename,"r"))) {
        fprintf(stderr, "acf_plugin error: CSV file not found: %s\n", filename);
        return 0;
    }


    while (fgets_unlocked (line1, sizeof(line1), file) != ((void *)0)) {
        lcount = 0;


        if (line1[strlen(line1) - 1] == '\n') {
            line1[strlen(line1) - 1] = '\0';
        }

        snprintf(line2, sizeof(line2), "%s", line1);
        stptr = line2;

        add_row(clist);


        if (line1[0] == '%')
            continue;


        while (*stptr != '\0') {
            lcount++;

            if (*stptr == '"') {
                int flag = 0;
                idx = 0;
                while (flag == 0) {
                    stptr++;

                    while (*stptr != '"') {
                        line3[idx] = *stptr;
                        idx++;
                        stptr++;
                    }
                    stptr++;
                    if (*stptr != '\0' && *stptr == ',') {
                        line3[idx] = '\0';
                        add_column(clist, line3);
                        flag = 1;
                    } else if (*stptr != '\0' && *stptr == '"') {
                        line3[idx] = *stptr;
                        idx++;
                    } else {
                        line3[idx] = '\0';
                        add_column(clist, line3);
                        flag = 1;
                    }
                }
            } else {
                idx = 0;
                while (*stptr != '\0' && *stptr != ',') {
                    line3[idx] = *stptr;
                    idx++;
                    stptr++;
                }
                line3[idx] = '\0';
                add_column(clist, line3);
            }
            if (*stptr != '\0' && *stptr == ',')
                stptr++;
            memmove(line2, stptr, strlen(stptr)+1);
            stptr = line2;
        }
    }

    return 1;
}

